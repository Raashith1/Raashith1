package oops;

public class Team {
	String TeamName ;
	int TeamMembers ;
	
	
	public String getTeamName() {
		return TeamName;
	}
	public void setTeamName(String teamName) {
		TeamName = teamName;
	}
	public int getTeamMembers() {
		return TeamMembers;
	}
	public void setTeamMembers(int teamMembers) {
		TeamMembers = teamMembers;
	}
}
